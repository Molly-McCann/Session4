public class IfElseExample {
    public static void main(String[] args) {
        int number = 10;

        // Check if the number is greater than 5
        if (number > 5) {
            System.out.println("The number is greater than 5.");
        } else {
            System.out.println("The number is not greater than 5.");
        }
    }
}

